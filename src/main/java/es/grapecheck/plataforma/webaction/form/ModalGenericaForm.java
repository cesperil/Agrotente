package es.grapecheck.plataforma.webaction.form;

import java.io.Serializable;

/**
 * @author rsanchez
 *
 */

public class ModalGenericaForm implements Serializable {

	private static final long serialVersionUID = 1L;
	protected Boolean mostrarModal;
	protected String tituloPrincipal;
	protected String texto;
	protected Boolean boton1;
	protected Boolean boton2;
	
	
	public Boolean getMostrarModal() {
		return mostrarModal;
	}
	public void setMostrarModal(Boolean mostrarModal) {
		this.mostrarModal = mostrarModal;
	}
	public String getTituloPrincipal() {
		return tituloPrincipal;
	}
	public void setTituloPrincipal(String tituloPrincipal) {
		this.tituloPrincipal = tituloPrincipal;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String texto) {
		this.texto = texto;
	}
	public Boolean getBoton1() {
		return boton1;
	}
	public void setBoton1(Boolean boton1) {
		this.boton1 = boton1;
	}
	public Boolean getBoton2() {
		return boton2;
	}
	public void setBoton2(Boolean boton2) {
		this.boton2 = boton2;
	}
	
}
